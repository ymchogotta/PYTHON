a = "Hello, World!"
print(a.upper())

a = "Hello, World!"
print(a.lower())

# strip() method removes any whitespace from the beginning or the end:
a = " Hello, World! " 
print(a.strip())

# replace string
a = "Hello, World!"
print(a.replace("H", "J"))

# split string
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']


